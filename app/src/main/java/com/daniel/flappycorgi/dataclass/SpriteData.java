package com.daniel.flappycorgi.dataclass;

public class SpriteData {
    public int x;
    public int y;
    public double xVelocity;
    public double yVelocity;

    public SpriteData(int x, int y, double xVelocity, double yVelocity) {
        this.x = x;
        this.y = y;
        this.xVelocity = xVelocity;
        this.yVelocity = yVelocity;
    }
}
